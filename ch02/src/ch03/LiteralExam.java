package ch03;


/*리터럴은 직접표현한 값*/
public class LiteralExam {

	public static void main(String[] args) {
	int x = 34;	//리터럴 - 34
	double y =42.195;	// 리터럴 42.195
	char c = '%';	//리터럴 % 	- 문자타입 변수c를 선언하고 문자 '%' 를 대입한 명령문
	boolean b = true; 	//리터럴true 	= boolean타입 변수 b를 선언하고boolean값 true를 대입한 명령문
	String message = "hello";	// 리터럴 "hello" - 문자열타입 변수 message를 선언하고 문자열 "hello"를 대입한 명령문	
	
	double result = x + y;	//리터럴이 없음 변수만존재 - double타입 변수를 result를 선언하고 변수x의 값과 
												// 변수y의 값을 더하여 결과를 대입한 명령문.
	
		// (예시)
		x = 20; //리터럴 - 직접입력한 값.
		y = 3.14;//리터럴 - 직접입력한 값.
	
	}

}
